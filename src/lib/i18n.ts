let currentLocale: Locale = (typeof window !== 'undefined'
  ? localStorage.getItem('locale') as Locale
  : null) || detectBrowserLanguage()

function detectBrowserLanguage(): Locale {
  if (typeof window === 'undefined') return 'en'
  const browserLang = navigator.language.toLowerCase()
  if (browserLang.startsWith('zh')) return 'zh'
  if (browserLang.startsWith('ja')) return 'ja'
  if (browserLang.startsWith('ko')) return 'ko'
  if (browserLang.startsWith('de')) return 'de'
  if (browserLang.startsWith('fr')) return 'fr'
  if (browserLang.startsWith('es')) return 'es'
  if (browserLang.startsWith('pt')) return 'pt'
  return 'en'
}

export function setLocale(locale: Locale) {
  currentLocale = locale
  localStorage.setItem('locale', locale)
  window.location.reload()
}

export function getLocale(): Locale {
  return currentLocale
}

export function t(key: keyof TranslationKeys, vars?: Record<string, string>): string {
  let str = translations[currentLocale]?.[key] || translations.en[key] || key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v)
    }
  }
  return str
}
